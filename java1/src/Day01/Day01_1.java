package Day01; // 패키지명 : 클래스 파일들의 묶음

public class Day01_1 { // 클래스 { : 시작 } : 끝
	// public : 접근제한자
		// class : 키워드 [클래스 선언 시 사용되는 키워드(미리 만들어진 단어)]
			// 클래스명 : 첫 글자는 무조건 대문자
		
		// main + ctrl + 스페이스바 => enter
		// main 함수 : main 스레드 포함 [실행코드]
			// 스레드 : 코드 읽어주는 역할 [cpu 처리]
	
	public static void main(String[] args) { // main 메소드
		// public : 접근제한자
			// static : 정적
				// void : 반환타입 없음
					// main : 함수이름
						// 인수 (String[] args)
		
		// syso + ctrl + 스페이스바 => enter
		System.out.println("java 시작");
			// System : 시스템 클래스
				// out : 시스템 클래스 내 메소드
					// println("출력내용") : 시스템 클래스 내 출력 메소드
						// ; : 한줄 코드 종료
			// . : 접근연산자 [클래스 내 변수나 함수 접근할 때 사용]
		
	} // main 메소드 close
} // Day01_1 클래스 close
